// Question: Write a program that prints the numbers from 1 to 5 using a while loop.

let num = 1;
while (num <= 5) {
  console.log(num);
  num++;
}

// Question: Write a program that prints the numbers from 1 to 5 using a do while loop.

let index = 1;
do {
  console.log(index);
  index++;
} while (index <= 5);
